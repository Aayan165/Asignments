// DATA TYPES (ASSIGNMENT)

// STRING
let place : string = "karachi";
console.log(place)

// NUMBERS
let age : number = 17;
console.log(age)

// BOOLEAN
let student : boolean = true;
console.log(student)

// NULL
let nothing : null = null;
console.log(nothing)

// UNDEFINED
let undefinedvalue : undefined = undefined;
console.log(undefinedvalue)

// SYMBOL
let symbolexample = Symbol("symbols are used to create private or unique object properties.");
let thing = {
    [symbolexample]: 'value'
};
console.log(thing[symbolexample])

// BIGINT
let large = BigInt ("9007199254740991");
console.log(large)

