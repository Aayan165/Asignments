//Assignment - CASES
// Camel case
// In camel case the first letter of 2nd word is capital only.
var myName = "Syed Aayan Mahmood";
console.log(myName);
// Snake case
// In snake case teo words are differentiated by using underscore (_) between them.
var my_name = "Syed Aayan Mahmood";
console.log(my_name);
// Pascal case
// In pascal case the first letter of every word is capital.
var MyName = "Syed Aayan Mahmood";
console.log(MyName);
